# 在Python中，可对整数执行加（+ ）减（- ）乘（* ）除（/ ）运算
a = 3 + 2  # 5
b = 3 - 2  # 1
c = 3 * 2  # 6
d = 3 // 2  # 1
e = 2 ** 3  # 8
f = (2 + 3) * 4  # 20
# 空格不影响Python计算表达式的方式，它们的存在旨在让你阅读代码时，能迅速确定先执行哪些运算。


# 浮点数
# Python将带小数点的数字都称为浮点
# 很大程度上说，使用浮点数时都无需考虑其行为
a1=0.1+0.1   # 0.2
b1=2*0.2     # 0.4
# 但需要注意的是，结果包含的小数位数可能是不确定的：
a2=0.2 + 0.1    # 0.30000000000000004
n2=3 * 0.1      # 0.30000000000000004


